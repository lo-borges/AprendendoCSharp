using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _9_WhileToFor
{
    internal class Program
    {
        static void Main(string[] args)
        {
            for (int contador = 1; contador <= 10; contador++)
            {
                Console.WriteLine(contador);
            }
            Console.ReadLine();
        }
    }
}
